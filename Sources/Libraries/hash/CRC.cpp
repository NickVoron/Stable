void make_crc8_table( void ) {
	int i, j;
	unsigned long poly, c;
	
	static const byte p[] = {0,1,2};

	
	poly = 0L;
	for ( i = 0; i < sizeof( p ) / sizeof( byte ); i++ ) {
		poly |= 1L << p[i];
	}

	for ( i = 0; i < 256; i++ ) {
		c = i;
		for ( j = 0; j < 8; j++ ) {
			c = ( c & 0x80 ) ? poly ^ ( c << 1 ) : ( c << 1 );
		}
		crctable[i] = (byte) c;
	}
}


void make_crc16_table( void ) {
	int i, j;
	unsigned long poly, c;
	static const byte p[] = {0,5,12};

	poly = 0L;
	for ( i = 0; i < sizeof( p ) / sizeof( byte ); i++ ) {
		poly |= 1L << p[i];
	}

	for ( i = 0; i < 256; i++ ) {
		c = i << 8;
		for ( j = 0; j < 8; j++ ) {
			c = ( c & 0x8000 ) ? poly ^ ( c << 1 ) : ( c << 1 );
		}
		crctable[i] = (unsigned short) c;
	}
}

void make_crc32_table( void ) {
	int i, j;
	unsigned long c, poly;
	static const byte p[] = {0,1,2,4,5,7,8,10,11,12,16,22,23,26};

	poly = 0L;
	for ( i = 0; i < sizeof( p ) / sizeof( byte ); i++ ) {
		poly |= 1L << ( 31 - p[i] );
	}

	for ( i = 0; i < 256; i++ ) {
		c = (unsigned long)i;
		for ( j = 0; j < 8; j++ ) {
			c = ( c & 1 ) ? poly ^ ( c >> 1 ) : ( c >> 1 );
		}
		crctable[i] = c;
	}
}

