unsigned char CRC8( const void *data, std::size_t len )
{
	unsigned char crc = CRC8_INIT;

	const unsigned char *buf = (const unsigned char *) data;
	while( len-- ) crc = crc8table[crc ^ *buf++];
	
	return crc ^= CRC8_XOR;
}

unsigned short CRC16( const void *data, std::size_t len )
{
	unsigned short crc = CRC16_INIT;

	const unsigned char *buf = (const unsigned char *) data;
	while( len-- ) crc = crc16table[crc ^ *buf++];

	return crc ^= CRC16_XOR;
}

unsigned long CRC32( const void *data, std::size_t len )
{
	unsigned long crc = CRC32_INIT;;

	const unsigned char *buf = (const unsigned char *) data;
	while( len-- ) 
	{
		crc = crc32table[ ( crc ^ ( *buf++ ) ) & 0xff ] ^ ( crc >> 8 );
	}

	return crc ^= CRC32_XOR;
}

